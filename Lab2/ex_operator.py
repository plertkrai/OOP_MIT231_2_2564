# operators

# matimatic operators
x, y = 100, 200
print(x, y)
# +
print(x+y)
z = x+y
print('The summation of x and y is ', z)

# -
print(x-y)  # 100-200 = -100
z = x-y
print(z)

# * (multiply)
z = x*y  # 100*200 = 20000
print(z)

# / (divide)
z = x/y  # 100/200  --> int/int --> float
print(z, type(z))
z = 10/5  # 2
print(z, type(z))

# ** (exponential) เลขชี้กำลัง
z = x**3  # 100 * 100 * 100
print(z)

# % (modulus:mod) หารเอาเศษ
z = x%y  # 100//200
print(z)

# // (floor division) หารแบบปัดเศษทิ้ง
z = x//y #  100//200 = 0
print(z)
z = 10//3 # 3 (calculator --> 3.33)
print(z)



